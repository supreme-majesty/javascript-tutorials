// Initialize a counter
let count = 1;

// While loop runs as long as the condition is true
while (count <= 5) {
  console.log("Count is:", count); // n - 1; n = 6 - 1= 5
  count++; // increase count by 1 each time
}

console.log("Loop finished!");

